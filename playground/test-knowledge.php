 * вижу что-то, ощущаю что-то, делаю то-то
 *
 * 1)Получаем рецептивное поле
 * 2)Активируем эмоцию
 * 3)Находим цепочки так, чтоб активатор эфферента(действие) прямо подавлял/стимулировал интернейрон(эмоция).
 * 5)Активатор эфферента воздействует на эфферент.
 *   Эмоция осуществляет обратную положительную связь с соответсующим активатором.
 * 6)При достижении порога активации, эфферент вызывает дальнейшую цепочку, соответсвующей реакции.
 * 7)Реакция выражается в активации эфферентных нейронов(моторика) и интернейронов(сознание)
 *
 * Пример:
 * Я на кухне (но не понимаю еще этого) и ощущаю жажду.
 * Самое эффективное действие, в данном контексте, попить воды.
 * Как я принимаю решение?
 *
 * В данном примере, у меня постоянно активируются понятия:
 * -жажда
 * холодильник
 * стул
 * стол
 * тарелка
 * ложка
 * стакан
 *
 * От активированных понятий активируются новые понятия
 * 1) Цепочка создаваемая отделом зрения
 * Кластеризуем, по признаку отношения, объекты которые видим, в общие понятия
