 * Теперь у нас есть постоянно активированное понятие !кухня
 * Пространственные понятия очень-очень сильно и постоянно активируются (гиппокампом), при постоянном контексте!
 * Лимбическая система также постоянно активирует эмоциональные понятия.
 * Цепочка распространения активации понятий от лимбической системы, которая в последствии приведет к эффективному действию:
 * !жажда: пить: вода: стакан: посуда: !кухня
 * Два сильных понятия получают связь, причем достаточно короткую.
 * В такой цепочке происходит лавиннообразная положительная обратная связь между понятиями.
 * Понятие !пить становиться сильным, за счет усиления сильным понятием !жажда.
